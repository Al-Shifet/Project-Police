    private void txtCaseIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCaseIDKeyReleased
        // TODO add your handling code here:
        String ch = txtCaseID.getText();
        jComboBoxCaseID.removeAllItems();
        if (ch.equals("")) {
            jComboBoxCaseID.setVisible(false);
        } else {
            try {
                System.out.println(ch);
                
                Class.forName("com.mysql.jdbc.Driver");
                Connection conn = DriverManager.getConnection(DB_URL, USER, PASS);
                Statement st = (Statement) conn.createStatement();
                ResultSet rs = st
			.executeQuery("Select case_id from cases where case_id like '"+ ch + "%'");
                while (rs.next()) {
                    String case_id = rs.getString("case_id");
                    if (case_id.equals("")) {
			jComboBoxCaseID.addItem("");
			jComboBoxCaseID.setVisible(false);
                    }else {
			jComboBoxCaseID.addItem(rs.getString("case_id"));
			System.out.println(rs.getString("case_id"));
			jComboBoxCaseID.setVisible(true);
                    }
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(Reporter.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Reporter.class.getName()).log(Level.SEVERE, null, ex);
            }catch (Exception ex) {
            }
            
        }
    }//GEN-LAST:event_txtCaseIDKeyReleased

     public static boolean isInt(String s){
        for (int i = 0; i < s.length(); i++) {
            if (!Character.isDigit(s.charAt(i))) {
                return false;
            }
        }
        return true;
    }
