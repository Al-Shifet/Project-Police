    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here
        
        
        if (txtCaseID.getText().length()==0) {
            JOptionPane.showMessageDialog(null, "Please enter a valid case ID");
        }else if(txtFirstname.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Please enter a valid First Name");
        }else if(txtLastName.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Please enter a valid Last Name");
        }else if(txtPhone.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Please enter a valid National ID");
        }else if (txtNationalid.getText().length()==0) {
            JOptionPane.showMessageDialog(null, "Please enter a valid Phone Number");
        }

        String caseID = txtCaseID.getText();
        String firstName = txtFirstname.getText();
        String lastName = txtLastName.getText();
        String nationalID = txtCaseID.getText();
        String phone = txtNationalid.getText();

        if (!isInt(caseID)) {
            JOptionPane.showMessageDialog(null, "Please Enter a Valid Case ID");
            txtCaseID.setText("");
        } else if (isInt(firstName)) {
            JOptionPane.showMessageDialog(null, "Please Enter A Valid First Name");
            txtFirstname.setText("");
        }
        else if (isInt(lastName)) {
            JOptionPane.showMessageDialog(null, "Please Enter A Valid Last Name");
            txtLastName.setText("");
        }
        else if (!isInt(nationalID)) {
            JOptionPane.showMessageDialog(null, "Pleaase Enter a Valid Case ID");
            txtNationalid.setText("");
        }
        else if (!isInt(phone)) {
            JOptionPane.showMessageDialog(null, "Pleaase Enter a Valid Case ID");
            txtPhone.setText("");
        }else{
            try {

                Class.forName("com.mysql.jdbc.Driver");
                System.out.println("Connecting to a selected database...");
                //STEP 3: Open a connection
                Connection conn = DriverManager.getConnection(DB_URL, USER, PASS);
                System.out.println("Connected database successfully...");

                PreparedStatement statement = conn.prepareStatement("INSERT INTO "
                    + "reporter (case_id,reporter_firstname,reporter_lastname,"
                    + "reporter_phone_number,nationalid) VALUES('"+caseID+"',"
                    + "'"+firstName+"','"+lastName+"',"
                    + "'"+phone+"','"+nationalID+"')");
                statement.execute();
                System.out.println("Query Excecuted Succesfully");
                JOptionPane.showMessageDialog(null, "Insertion Succesful");
                PoliceHome police = new PoliceHome();
                police.setSize(775,497);
                police.setLocationRelativeTo(null);
                police.pack();
                police.setVisible(true);


            }catch (ClassNotFoundException | SQLException a) //catch block
            {
                a.printStackTrace();
            }

        }
    }//GEN-LAST:event_btnSubmitActionPerformed

