    private void jTable_Display_PoliceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_Display_PoliceMouseClicked
        // TODO add your handling code here:
        //Display Selected Row in JTextFields
        int i = jTable_Display_Police.getSelectedRow();
        TableModel model = jTable_Display_Police.getModel();
        txtPoliceID.setText(model.getValueAt(i, 0).toString());
        txtFirstname.setText(model.getValueAt(i, 1).toString());
        txtLastName.setText(model.getValueAt(i, 2).toString());
        txtPhone.setText(model.getValueAt(i, 3).toString());
        txtNationalid.setText(model.getValueAt(i, 4).toString());
        txtRank.setText(model.getValueAt(i, 5).toString());
        txtBadgeNumber.setText(model.getValueAt(i, 6).toString());
        password.setText(model.getValueAt(i, 7).toString());
        
    }//GEN-LAST:event_jTable_Display_PoliceMouseClicked

