     public Connection getConnection(){
        Connection conn;
        try {
            conn = DriverManager.getConnection(DB_URL, USER, PASS); 
            
            return conn;
        } catch (SQLException e) {
            return null;
        }
    }
    
    public ArrayList<PoliceOfficer> getPoliceList(){
        ArrayList<PoliceOfficer> policeList = new ArrayList<>();
        Connection connection = getConnection();
        
        String query = "SELECT * FROM police";
        Statement st;
        ResultSet rs;
        
        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            PoliceOfficer police;
            while (rs.next()) {                
                police = new PoliceOfficer(rs.getInt("police_id"),
                rs.getString("police_firstname"),rs.getString("police_lastname"),
                rs.getInt("police_phone_number"),rs.getString("nationalid"),
                rs.getString("rank"),
                rs.getString("badge_number"),rs.getString("password"));
                policeList.add(police);
            }
        } catch (SQLException e) {
        }
        return policeList;
    }
    
    //DISPLAY DATA IN THE JTABLE
    public void Show_Police_In_Table(){
        ArrayList<PoliceOfficer> list = getPoliceList();
        DefaultTableModel model = (DefaultTableModel) jTable_Display_Police.getModel();
        Object[] row = new Object[8];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getPoliceID();
            row[1] = list.get(i).getFirstName();
            row[2] = list.get(i).getLastName();
            row[3] = list.get(i).getPhoneNumber();
            row[4] = list.get(i).getNationalID();
            row[5] = list.get(i).getRank();
            row[6] = list.get(i).getBadgeNumber();
            row[7] = list.get(i).getPassword();
            
            model.addRow(row);
        }
    }
 
    
    public void excecuteSQLQuery(String query, String message){
        Connection con = getConnection();
        Statement st;
        try {
            st = con.createStatement();
            if ((st.executeUpdate(query) == 1)) {
                //Refresh the Data in the table
                DefaultTableModel model = (DefaultTableModel) jTable_Display_Police.getModel();
                model.setRowCount(0);
                Show_Police_In_Table();
                JOptionPane.showMessageDialog(null, "Data "+message+" Succesfully");
            }else{
               JOptionPane.showMessageDialog(null, "Data Not "+message);
            }
        } catch (Exception e) {
        }
    }
    

